     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $contacts = new contacts (); 
        $contacts->setAttribute('name', $request->get('name')); 
        $contacts->setAttribute('mail', $request->get('mail'));
        $contacts->setAttribute('message', $request->get('message'));
        $contacts->save(); 

        return redirect()->route('message')

        ->with('success', 'Votre message a été bien envoyé. Merci!');

        //
    }

